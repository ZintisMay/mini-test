function add(x, y) {
  return x + y;
}
function subtract(x, y) {
  return x - y;
}
function multiply(x, y) {
  return x * y;
}
function divide(x, y) {
  return x / y;
}
function exponent(x, y) {
  return x ** y;
}
function combineStrings(x, y) {
  return x + y;
}
function combineArrays(x, y) {
  return [...x, ...y];
}
function combineObjects(x, y) {
  return { ...x, ...y };
}
